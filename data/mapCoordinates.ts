// Get line path as array of coordinates for a given line
export function getLinePath(lineId: string): StationCoord[] {
  const prefix = lineId === '8A' ? '8A-' : `${lineId}-`;
  return Object.entries(stationCoords)
    .filter(([id]) => id.startsWith(prefix))
    .sort(([a], [b]) => {
      const numA = parseInt(a.split('-')[1]);
      const numB = parseInt(b.split('-')[1]);
      return numA - numB;
    })
    .map(([, coord]) => coord);
}
